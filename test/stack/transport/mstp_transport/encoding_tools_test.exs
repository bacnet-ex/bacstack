  test "validate header CRC on valid frame in binary" do
    assert 0x55 = EncodingTools.calculate_header_crc(<<0x00, 0x10, 0x05, 0x00, 0x00, 0x8C>>, 0xFF)
  end

  test "validate header CRC on valid frame in list" do
    assert 0x55 = EncodingTools.calculate_header_crc([0x00, 0x10, 0x05, 0x00, 0x00, 0x8C], 0xFF)
  end

  test "validate header CRC on valid frame in unicode list raises" do
    assert_raise FunctionClauseError, fn ->
      EncodingTools.calculate_header_crc([0x00, 0x1005, 0x00, 0x00, 0x8C], 0xFF)
    end
  end

  test "validate header CRC on valid frame in improper list" do
    assert 0x55 = EncodingTools.calculate_header_crc([0x00, 0x10, 0x05, 0x00, 0x00 | 0x8C])
  end

  test "validate header CRC on valid frame repeated" do
    assert 0x55 =
             Enum.reduce(
               [0x00, 0x10, 0x05, 0x00, 0x00, 0x8C],
               0xFF,
               &EncodingTools.calculate_header_crc(&1, &2)
             )
  end

  test "validate data CRC on valid frame in binary" do
    assert 0xF0B8 = EncodingTools.calculate_data_crc(<<0x01, 0x22, 0x30, 0x10, 0xBD>>, 0xFFFF)
  end

  test "validate data CRC on valid frame in list" do
    assert 0xF0B8 = EncodingTools.calculate_data_crc([0x01, 0x22, 0x30, 0x10, 0xBD], 0xFFFF)
  end

  test "validate data CRC on valid frame in unicode list raises" do
    assert_raise FunctionClauseError, fn ->
      EncodingTools.calculate_data_crc([0x01, 0x22, 0x30, 0x10BD], 0xFFFF)
    end
  end

  test "validate data CRC on valid frame in improper list" do
    assert 0xF0B8 = EncodingTools.calculate_data_crc([0x01, 0x22, 0x30, 0x10 | 0xBD])
  end

  test "validate data CRC on valid frame repeated" do
    assert 0xF0B8 =
             Enum.reduce(
               [0x01, 0x22, 0x30, 0x10, 0xBD],
               0xFFFF,
               &EncodingTools.calculate_data_crc(&1, &2)
             )
  end

  test "do COBS decoding on valid frame in binary" do
    payload = IO.iodata_to_binary(cobs_decode_test_payload_nested_iolist())

    assert {:ok, packet} = EncodingTools.decode_cobs(payload)
    assert [1, 32, 255, 255 | _tl] = Enum.take(packet, 20)

    # Make sure we can decode the packet
    assert {:ok, {%NPCI{} = _npdu, raw_apdu}} = Protocol.decode_npci(IO.iodata_to_binary(packet))
    assert {:ok, %UnconfirmedServiceRequest{} = apdu} = APDU.decode(raw_apdu)
    assert {:ok, %WhoHas{} = service} = WhoHas.from_apdu(apdu)

    # 26 letters à 19 times
    assert byte_size(service.object) == 26 * 19
  end

  test "do COBS decoding on valid frame in iolist" do
    payload = List.flatten(cobs_decode_test_payload_nested_iolist())

    assert {:ok, packet} = EncodingTools.decode_cobs(payload)
    assert [1, 32, 255, 255 | _tl] = Enum.take(packet, 20)

    # Make sure we can decode the packet
    assert {:ok, {%NPCI{} = _npdu, raw_apdu}} = Protocol.decode_npci(IO.iodata_to_binary(packet))
    assert {:ok, %UnconfirmedServiceRequest{} = apdu} = APDU.decode(raw_apdu)
    assert {:ok, %WhoHas{} = service} = WhoHas.from_apdu(apdu)

    # 26 letters à 19 times
    assert byte_size(service.object) == 26 * 19
  end

  test "do COBS decoding on valid frame in nested iolist" do
    payload = cobs_decode_test_payload_nested_iolist()

    assert {:ok, packet} = EncodingTools.decode_cobs(payload)
    assert [1, 32, 255, 255 | _tl] = Enum.take(packet, 20)

    # Make sure we can decode the packet
    assert {:ok, {%NPCI{} = _npdu, raw_apdu}} = Protocol.decode_npci(IO.iodata_to_binary(packet))
    assert {:ok, %UnconfirmedServiceRequest{} = apdu} = APDU.decode(raw_apdu)
    assert {:ok, %WhoHas{} = service} = WhoHas.from_apdu(apdu)

    # 26 letters à 19 times
    assert byte_size(service.object) == 26 * 19

    assert Map.new(?A..?Z, &{&1, 19}) ==
             Enum.frequencies(:binary.bin_to_list(service.object))
  end

  test "do COBS decoding on invalid frame" do
    payload = <<0x55, 0xFF>>

    assert {:error, :invalid_crc32k_length} = EncodingTools.decode_cobs(payload)
  end

  test "do COBS decoding with invalid CRC" do
    payload = <<0x01, 0x22, 0x30, 80, 235, 240, 119, 41, 80, 138, 93, 202, 217>>

    assert {:error, :crc32k_mismatch} = EncodingTools.decode_cobs(payload)
  end

  test "do COBS decoding with invalid CRC length" do
    payload = <<81, 84, 119, 101, 0, 3, 45, 3, 137>>

    assert {:error, :invalid_crc32k_length} = EncodingTools.decode_cobs(payload)
  end

